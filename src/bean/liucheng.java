package bean;

public class liucheng {
	private String liuCnum;//流程单号
	private String uid;//用户名
	private String ddanNum;//订单编号
	private String gunum;//固件编号
	public String getLiuCnum() {
		return liuCnum;
	}
	public void setLiuCnum(String liuCnum) {
		this.liuCnum = liuCnum;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getDdanNum() {
		return ddanNum;
	}
	public void setDdanNum(String ddanNum) {
		this.ddanNum = ddanNum;
	}
	public String getGunum() {
		return gunum;
	}
	public void setGunum(String gunum) {
		this.gunum = gunum;
	}
	
	
}
